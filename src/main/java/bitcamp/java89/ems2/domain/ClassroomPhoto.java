package bitcamp.java89.ems2.domain;

public class ClassroomPhoto {
  
  protected int croomPhotoNo;
  protected int classroomNo;
  protected String path;
  
  public int getClassroomPhotoNo() {
    return croomPhotoNo;
  }
  public void setClassroomPhotoNo(int croomPhotoNo) {
    this.croomPhotoNo = croomPhotoNo;
  }
  public int getClassroomNo() {
    return classroomNo;
  }
  public void setClassroomNo(int classroomNo) {
    this.classroomNo = classroomNo;
  }
  public String getPath() {
    return path;
  }
  public void setPath(String path) {
    this.path = path;
  }
  
}
